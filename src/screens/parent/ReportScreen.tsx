type RangeOption = '7d' | '30d' | '90d' | 'all';

export default function ReportScreen() {
  const { parentProfile, moodEntries, sessions, routines, medications, teacherContacts, teacherNotes } =
    useAppStore();

  const children = parentProfile?.childProfiles ?? [];
  const [selectedChildId, setSelectedChildId] = useState(children[0]?.id ?? '');
  const [range, setRange] = useState<RangeOption>('30d');
  const [exporting, setExporting] = useState(false);

  const selectedChild = children.find((c) => c.id === selectedChildId);

  const cutoff = (): Date => {
    const d = new Date();
    if (range === '7d') d.setDate(d.getDate() - 7);
    else if (range === '30d') d.setDate(d.getDate() - 30);
    else if (range === '90d') d.setDate(d.getDate() - 90);
    else d.setFullYear(2000);
    return d;
  };

  const filter = <T extends { [k: string]: any }>(arr: T[], dateKey: string) =>
    arr.filter((x) => x.childProfileId === selectedChildId && new Date(x[dateKey]) >= cutoff());

  const childSessions = sessions.filter(
    (s) => s.childProfileId === selectedChildId && new Date(s.startedAt) >= cutoff()
  );
  const childMoods = filter(moodEntries, 'recordedAt');
  const childMeds = medications.filter((m) => m.childProfileId === selectedChildId);
  const childNotes = filter(teacherNotes, 'date');
  const childContacts = teacherContacts.filter((c) => c.childProfileId === selectedChildId);

  const rangeLabel = () => {
    const now = new Date();
    const from = cutoff();
    if (range === 'all') return 'All time';
    return `${from.toLocaleDateString()} – ${now.toLocaleDateString()}`;
  };

  const handleExport = async () => {
    if (!selectedChild) return;
    setExporting(true);
    try {
      const sections: ReportSection[] = [
        routineSection(childSessions, routines),
        moodSection(childMoods),
        medicationSection(childMeds),
        teacherSection(childNotes, childContacts),
      ];
      const html = buildHtml(selectedChild.name, selectedChild.avatarEmoji, rangeLabel(), sections);
      const { uri } = await Print.printToFileAsync({ html, base64: false });

      const canShare = await Sharing.isAvailableAsync();
      if (canShare) {
        await Sharing.shareAsync(uri, {
          mimeType: 'application/pdf',
          dialogTitle: `${selectedChild.name}'s Progress Report`,
          UTI: 'com.adobe.pdf',
        });
      } else {
        Alert.alert('PDF saved', `Report saved to: ${uri}`);
      }
    } catch (e: any) {
      Alert.alert('Export failed', e.message ?? 'Unknown error');
    } finally {
      setExporting(false);
    }
  };

  const RANGES: { label: string; value: RangeOption }[] = [
    { label: '7 days', value: '7d' },
    { label: '30 days', value: '30d' },
    { label: '90 days', value: '90d' },
    { label: 'All time', value: 'all' },
  ];

  return (
    <SafeAreaView style={styles.container}>
      <ScrollView contentContainerStyle={styles.scroll} showsVerticalScrollIndicator={false}>
        <Text style={styles.screenTitle}>Export Report</Text>
        <Text style={styles.subtitle}>
          Generate a PDF progress report to share with doctors, therapists, or schools
        </Text>

        {/* Child selector */}
        {children.length > 1 && (
          <ScrollView horizontal showsHorizontalScrollIndicator={false} style={styles.childSelector}>
            {children.map((child) => (
              <TouchableOpacity
                key={child.id}
                style={[styles.childChip, selectedChildId === child.id && styles.childChipActive]}
                onPress={() => setSelectedChildId(child.id)}
              >
                <Text>{child.avatarEmoji}</Text>
                <Text style={[styles.chipName, selectedChildId === child.id && styles.chipNameActive]}>
                  {child.name}
                </Text>
              </TouchableOpacity>
            ))}
          </ScrollView>
        )}

        {/* Date range */}
        <View style={styles.field}>
          <Text style={styles.label}>Date Range</Text>
          <View style={styles.rangeRow}>
            {RANGES.map((r) => (
              <TouchableOpacity
                key={r.value}
                style={[styles.rangeChip, range === r.value && styles.rangeChipActive]}
                onPress={() => setRange(r.value)}
              >
                <Text style={[styles.rangeChipText, range === r.value && styles.rangeChipTextActive]}>
                  {r.label}
                </Text>
              </TouchableOpacity>
            ))}
          </View>
        </View>

        {/* Report preview */}
        <View style={styles.previewCard}>
          <Text style={styles.previewTitle}>Report will include</Text>
          {[
            { emoji: '📋', label: 'Routines', count: `${childSessions.filter((s) => !!s.completedAt).length} completions` },
            { emoji: '😊', label: 'Mood & Behavior', count: `${childMoods.length} entries` },
            { emoji: '💊', label: 'Medication', count: `${childMeds.length} medications` },
            { emoji: '👩‍🏫', label: 'Teacher Notes', count: `${childNotes.length} notes` },
          ].map((item) => (
            <View key={item.emoji} style={styles.previewRow}>
              <Text style={styles.previewEmoji}>{item.emoji}</Text>
              <Text style={styles.previewLabel}>{item.label}</Text>
              <Text style={styles.previewCount}>{item.count}</Text>
            </View>
          ))}
          <Text style={styles.previewRange}>Period: {rangeLabel()}</Text>
        </View>

        <TouchableOpacity
          style={[styles.exportBtn, exporting && styles.exportBtnLoading]}
          onPress={handleExport}
          disabled={exporting}
        >
          {exporting ? (
            <ActivityIndicator color="#fff" />
          ) : (
            <Text style={styles.exportBtnText}>📄 Export PDF Report</Text>
          )}
        </TouchableOpacity>
      </ScrollView>
    </SafeAreaView>
  );
}

