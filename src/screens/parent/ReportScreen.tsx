// Section builders
// ---------------------------------------------------------------------------

function routineSection(sessions: any[], routines: any[]): ReportSection {
  const completed = sessions.filter((s) => !!s.completedAt);
  const rate = sessions.length > 0 ? Math.round((completed.length / sessions.length) * 100) : 0;

  const byRoutine: Record<string, { name: string; completions: number }> = {};
  for (const s of completed) {
    const r = routines.find((r: any) => r.id === s.routineId);
    if (r) {
      byRoutine[r.id] = byRoutine[r.id] ?? { name: r.name, completions: 0 };
      byRoutine[r.id].completions++;
    }
  }
  const rows = Object.values(byRoutine)
    .sort((a, b) => b.completions - a.completions)
    .map((r) => `<tr><td>${r.name}</td><td>${r.completions} times</td></tr>`)
    .join('');

  return {
    emoji: '📋',
    title: 'Routines',
    html: `
      <div class="stat-row">
        <div class="stat"><div class="stat-value">${completed.length}</div><div class="stat-label">Completed</div></div>
        <div class="stat"><div class="stat-value">${rate}%</div><div class="stat-label">Completion rate</div></div>
        <div class="stat"><div class="stat-value">${completed.reduce((a, s) => a + s.taskCompletions.length + 5, 0)}</div><div class="stat-label">Stars earned</div></div>
      </div>
      ${rows ? `<table style="margin-top:16px"><tr><th>Routine</th><th>Completions</th></tr>${rows}</table>` : ''}
    `,
  };
}

function moodSection(entries: any[]): ReportSection {
  if (entries.length === 0) {
    return { emoji: '😊', title: 'Mood & Behavior', html: '<p class="empty">No mood entries recorded.</p>' };
  }

  const avgMood = (entries.reduce((s, e) => s + e.mood, 0) / entries.length).toFixed(1);
  const avgFocus = (entries.reduce((s, e) => s + e.focus, 0) / entries.length).toFixed(1);
  const moodEmojis = ['', '😔', '😕', '😐', '🙂', '😄'];
  const focusEmojis = ['', '🌀', '😵', '😶', '🎯', '⚡'];

  const behaviorCounts: Record<string, number> = {};
  entries.forEach((e) =>
    e.behaviors.forEach((b: string) => { behaviorCounts[b] = (behaviorCounts[b] ?? 0) + 1; })
  );
  const sortedBehaviors = Object.entries(behaviorCounts).sort((a, b) => b[1] - a[1]);
  const behaviorBadges = sortedBehaviors
    .map(([b, n]) => `<span class="badge">${b} × ${n}</span>`)
    .join(' ');

  const recent = entries
    .slice(0, 7)
    .map((e) => `<tr><td>${new Date(e.recordedAt).toLocaleDateString()}</td><td>${moodEmojis[e.mood]} ${e.mood}/5</td><td>${focusEmojis[e.focus]} ${e.focus}/5</td><td>${e.behaviors.join(', ') || '—'}</td></tr>`)
    .join('');

  return {
    emoji: '😊',
    title: 'Mood & Behavior',
    html: `
      <div class="stat-row">
        <div class="stat"><div class="stat-value">${entries.length}</div><div class="stat-label">Entries</div></div>
        <div class="stat"><div class="stat-value">${avgMood}/5</div><div class="stat-label">Avg Mood</div></div>
        <div class="stat"><div class="stat-value">${avgFocus}/5</div><div class="stat-label">Avg Focus</div></div>
      </div>
      ${behaviorBadges ? `<div style="margin-top:16px"><strong style="font-size:12px;color:#6B6B8D">Observed behaviors</strong><div style="margin-top:8px">${behaviorBadges}</div></div>` : ''}
      <table style="margin-top:16px">
        <tr><th>Date</th><th>Mood</th><th>Focus</th><th>Behaviors</th></tr>
        ${recent}
      </table>
    `,
  };
}

function medicationSection(medications: any[]): ReportSection {
  if (medications.length === 0) {
    return { emoji: '💊', title: 'Medication', html: '<p class="empty">No medications recorded.</p>' };
  }

  const rows = medications.map((med) => {
    const totalLogs = med.logHistory.length;
    const takenLogs = med.logHistory.filter((l: any) => !l.skipped).length;
    const adherence = totalLogs > 0 ? Math.round((takenLogs / totalLogs) * 100) : 0;
    return `<tr>
      <td><strong>${med.name}</strong>${med.dosage ? ` — ${med.dosage}` : ''}</td>
      <td>${med.times.join(', ')}</td>
      <td>${totalLogs > 0 ? `${adherence}% (${takenLogs}/${totalLogs})` : 'No logs yet'}</td>
    </tr>`;
  }).join('');

  return {
    emoji: '💊',
    title: 'Medication',
    html: `<table><tr><th>Medication</th><th>Times</th><th>Adherence</th></tr>${rows}</table>`,
  };
}

function teacherSection(notes: any[], contacts: any[]): ReportSection {
  if (notes.length === 0) {
    return { emoji: '👩‍🏫', title: 'Teacher Notes', html: '<p class="empty">No teacher notes recorded.</p>' };
  }

  const positives = notes.filter((n) => n.type === 'positive').length;
  const concerns = notes.filter((n) => n.type === 'concern').length;

  const rows = notes.slice(0, 10).map((n) => {
    const contact = contacts.find((c: any) => c.id === n.teacherContactId);
    const typeClass = n.type === 'positive' ? 'badge-positive' : n.type === 'concern' ? 'badge-concern' : '';
    return `<tr>
      <td>${new Date(n.date).toLocaleDateString()}</td>
      <td><span class="badge ${typeClass}">${n.type}</span></td>
      <td>${contact?.name ?? 'General'}</td>
      <td>${n.content}</td>
    </tr>`;
  }).join('');

  return {
    emoji: '👩‍🏫',
    title: 'Teacher Notes',
    html: `
      <div class="stat-row">
        <div class="stat"><div class="stat-value">${notes.length}</div><div class="stat-label">Total notes</div></div>
        <div class="stat"><div class="stat-value">${positives}</div><div class="stat-label">Positives</div></div>
        <div class="stat"><div class="stat-value">${concerns}</div><div class="stat-label">Concerns</div></div>
      </div>
      <table style="margin-top:16px"><tr><th>Date</th><th>Type</th><th>From</th><th>Note</th></tr>${rows}</table>
    `,
  };
}

